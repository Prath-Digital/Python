numbers = [4, 1, 7, 3, 9, 2]
length = len(numbers)  # Get the length of the list
maximum = max(numbers)  # Find the maximum value
sorted_list = sorted(numbers)  # Sort the list
total = sum(numbers)  # Calculate the sum
data_type = type(numbers)  # Check the type of the list

print(f"Length: {length}")
print(f"Maximum: {maximum}")
print(f"Sorted: {sorted_list}")
print(f"Sum: {total}")
print(f"Type: {data_type}")